    private void botao_alugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_alugarActionPerformed
//        verificando seleção na tabela clientes     verificando selação na tabela carros   
        if (tabelaClientes.getSelectedRow() != -1 && tabelaCarros.getSelectedRow() != -1) {
//            System.out.println("Linha selecionada: " + tabelaClientes.getSelectedRow());
//            System.out.println("ID da linha selecionada: " + Integer.parseInt(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(),0).toString()));

            ConfirmaAluguel confALu = new ConfirmaAluguel();
            TelaPrincipal.jDesktopPane1.add(confALu);
            confALu.setVisible(true);
            confALu.setPosicao();

            Cliente cliente = new Cliente();
            PesquisaClienteDAO pesCliDao = new PesquisaClienteDAO();
            cliente = pesCliDao.readCli(Integer.parseInt(tabelaClientes.getValueAt(tabelaClientes.getSelectedRow(), 0).toString()));
            
            ConfirmaAluguel.campo_nome.setText(cliente.getNome());
            ConfirmaAluguel.campo_endereco.setText(cliente.getEndereco());
            ConfirmaAluguel.campo_identidade.setText(cliente.getRg());
            ConfirmaAluguel.campo_cpf.setText(cliente.getCpf());
            ConfirmaAluguel.campo_telefone.setText(cliente.getTelefone());
            ConfirmaAluguel.campo_celular.setText(cliente.getCelular());
            ConfirmaAluguel.campo_email.setText(cliente.getEmail());
            ConfirmaAluguel.campo_dtNasc.setText(cliente.getDtNasc());
            ConfirmaAluguel.campo_idCli.setText(Integer.toString(cliente.getId()));
            ConfirmaAluguel.campo_idCli.setVisible(false);
            
            Carro carro = new Carro();
            PesquisaCarroDAO pesCarDao = new PesquisaCarroDAO();
            carro = pesCarDao.readCar(Integer.parseInt(tabelaCarros.getValueAt(tabelaCarros.getSelectedRow(), 0).toString()));
            Date dataSistema = new Date();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            
            ConfirmaAluguel.campo_fabricante.setText(carro.getFabricante());
            ConfirmaAluguel.campo_modelo.setText(carro.getModelo());
            ConfirmaAluguel.campo_cor.setText(carro.getCor());
            ConfirmaAluguel.campo_tpComb.setText(carro.getTpCombustivel());
            ConfirmaAluguel.campo_motor.setText(carro.getMotor());
            ConfirmaAluguel.campo_km.setText(carro.getKmAtual());
            ConfirmaAluguel.campo_dtAluguel.setText(formato.format(dataSistema));
            ConfirmaAluguel.campo_idCar.setText(Integer.toString(carro.getId()));
            ConfirmaAluguel.campo_idCar.setVisible(false);

            this.setVisible(false);

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente e um carro para prosseguir..");
        }
    }//GEN-LAST:event_botao_alugarActionPerformed


