    private void campo_fabricKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_fabricKeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER && campo_fabric.getText().isEmpty()) {
            campo_modelo.requestFocus();
        } else if (evt.getKeyCode() == evt.VK_ENTER && !campo_fabric.getText().isEmpty()) {
            readForFabricante(campo_fabric.getText());
            campo_fabric.selectAll();
        } else if (evt.getKeyCode() == evt.VK_DELETE && campo_fabric.getText().isEmpty()) {
            readTable();
        } else if (evt.getKeyCode() == evt.VK_DELETE) {
            readTable();
        }
    }//GEN-LAST:event_campo_fabricKeyPressed

