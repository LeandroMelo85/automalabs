    public void readTableCliente() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaClientes.getModel();
        modelo.setNumRows(0);
        ClienteDAO cliDAO = new ClienteDAO();

        for (Cliente cli : cliDAO.read()) {

            modelo.addRow(new Object[]{
                cli.getId(),
                cli.getNome(),
                cli.getEndereco(),
                cli.getTelefone(),
                cli.getCelular(),
                cli.getDtNasc(),
                cli.getRg(),
                cli.getCpf(),
                cli.getEmail(),
            });

        }
    }
    public void readTableCarDisp() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaCarros.getModel();
        modelo.setNumRows(0);
        PesquisaCarroDAO pesqDAO = new PesquisaCarroDAO();

        for (Carro car : pesqDAO.carrosDisponiveis()) {

            modelo.addRow(new Object[]{
                car.getId(),
                car.getFabricante(),
                car.getModelo(),
                car.getCor(),
                car.getMotor(),
                car.getTpCombustivel(),
                car.getKmAtual(),
                car.getDtCadastro(),
                car.getAlugado(),
                car.getDtAluguel(),
                car.getDtDevolucao(),
            });

        }
    }
