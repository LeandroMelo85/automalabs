    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaCarros.getModel();
        modelo.setNumRows(0);
        CarroDAO carDAO = new CarroDAO();

        for (Carro car : carDAO.read()) {

            modelo.addRow(new Object[]{
                car.getId(),
                car.getFabricante(),
                car.getModelo(),
                car.getCor(),
                car.getMotor(),
                car.getTpCombustivel(),
                car.getKmAtual(),
                car.getDtCadastro(),
                car.getAlugado(),
                car.getDtAluguel(),
                car.getDtDevolucao(),
            });

        }
    }
    
